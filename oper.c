#include<stdio.h>
void main()
{
    int n1=20;
    int n2=10;
    int add=n1+n2;
    int sub=n1-n2;
    int prod=n1*n2;
    float quo=n1/n2;
    float rem=n1%n2;
    printf("addition: %d \n",add);
    printf("subtraction: %d \n",sub);
    printf("product: %d \n",prod);
    printf("division(Q): %f \n",quo);
    printf("division(R): %f \n",rem);
}